#include <cstdio>

char letters[5][5][4] = {{"***", "*.*", "***", "*.*", "*.*"},
						 {"***", "*.*", "***", "*.*", "***"},
						 {"***", "*..", "*..", "*..", "***"},
						 {"***", "*.*", "*.*", "*.*", "***"},
						 {"***", "*..", "***", "*..", "***"}};

int main(){
	int n;
	scanf("%d", &n);
	char s[2018];
	scanf("%s", s);
	for(int i = 0; i < 5; ++i){
		for(int j = 0; j < n; ++j){
			printf("%s", letters[s[j] - 'A'][i]);
		}
		printf("\n");
	}
	return 0;
}
